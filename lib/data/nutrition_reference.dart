  /// 统一查表方法
  ///
  /// 根据性别、身高、体重、是否做力量训练、健身目标查询对应的系数。
  /// 返回 (训练日碳水, 休息日碳水, 蛋白质) g/kg，或 null（无匹配数据）。
  static (double, double, double)? lookupFactor({
    required bool isMale,
    required int heightCm,
    required int weightKg,
    required bool isStrengthTraining,
    required FitnessGoal goal,
  }) {
    final Map<int, Map<int, dynamic>> table;
    if (!isStrengthTraining) {
      // 无力量训练 → 使用 nutritionMale / nutritionFemale（旧格式 2-tuple）
      table = isMale ? nutritionMale : nutritionFemale;
    } else if (goal == FitnessGoal.fatLoss) {
      table = isMale ? maleFatLoss : femaleFatLoss;
    } else {
      table = isMale ? maleMuscleGain : femaleMuscleGain;
    }

    // 尝试精确匹配身高
    final heightData = table[heightCm];
    if (heightData != null) {
      final val = heightData[weightKg];
      if (val != null) {
        return _normalizeFactor(val);
      }
    }

    // 尝试找最接近的身高，再找最接近的体重
    final sortedHeights = table.keys.toList()..sort((a, b) {
      return (a - heightCm).abs().compareTo((b - heightCm).abs());
    });
    for (final h in sortedHeights) {
      final weightData = table[h]!;
      // 精确体重匹配
      final val = weightData[weightKg];
      if (val != null) {
        return _normalizeFactor(val);
      }
      // 最近体重匹配
      final sortedWeights = weightData.keys.toList()..sort((a, b) {
        return (a - weightKg).abs().compareTo((b - weightKg).abs());
      });
      for (final w in sortedWeights) {
        final v = weightData[w];
        if (v != null) {
          return _normalizeFactor(v);
        }
      }
    }

    return null;
  }

  /// 将 2-tuple 或 3-tuple 统一转为 3-tuple
  static (double, double, double) _normalizeFactor(dynamic val) {
    if (val is (double, double, double)) return val;
    if (val is (double, double)) return (val.$1, val.$1, val.$2);
    throw ArgumentError('Unexpected factor type: ${val.runtimeType}');
  }

  /// 获取推荐因子（使用训练日碳水和蛋白质）
  ///
  /// 返回 (carbsPerKg, proteinPerKg) 或 null（无匹配）。
  static (double carbsPerKg, double proteinPerKg)? lookupRecommended({
    required bool isMale,
    required int heightCm,
    required int weightKg,
    required bool isStrengthTraining,
    required FitnessGoal goal,
  }) {
    final result = lookupFactor(
      isMale: isMale,
      heightCm: heightCm,
      weightKg: weightKg,
      isStrengthTraining: isStrengthTraining,
      goal: goal,
    );
    if (result == null) return null;
    // 使用训练日碳水和蛋白质
    return (result.$1, result.$3);
  }

  /// 获取推荐说明文字
  static String recommendationNote({
    required bool isMale,
    required int heightCm,
    required int weightKg,
    required bool isStrengthTraining,
    required FitnessGoal goal,
  }) {
    final result = lookupFactor(
      isMale: isMale,
      heightCm: heightCm,
      weightKg: weightKg,
      isStrengthTraining: isStrengthTraining,
      goal: goal,
    );
    if (result == null) return '当前身高/体重组合暂无参考数据';

    final (trainingCarbs, _, protein) = result;
    final dailyProtein = protein * weightKg.toDouble();
    final dailyCarbs = trainingCarbs * weightKg.toDouble();
    final genderLabel = isMale ? '男' : '女';
    final scenario = _scenarioLabel(isStrengthTraining, goal);

    return '基于$scenario / 身高${heightCm}cm / 体重${weightKg}kg / $genderLabel 推荐：'
        '蛋白质 ${protein.toStringAsFixed(1)} g/kg（每日 ${dailyProtein.toStringAsFixed(0)} g），'
        '碳水 ${trainingCarbs.toStringAsFixed(1)} g/kg（每日 ${dailyCarbs.toStringAsFixed(0)} g）';
  }

  /// 场景标签
  static String _scenarioLabel(bool isStrengthTraining, FitnessGoal goal) {
    if (goal == FitnessGoal.fatLoss && !isStrengthTraining) {
      return '减脂（纯饮食控制）';
    }
    if (goal == FitnessGoal.fatLoss) {
      return '减脂（力量训练）';
    }
    return '增肌（力量训练）';
  }

  /// 获取当前场景下的完整身高×体重对照表。
  ///
  /// 返回 Map<身高cm, Map<体重kg, (训练日碳水, 休息日碳水, 蛋白质)?>>。
  /// null 值表示该身高/体重组合无可参考数据（大多为过轻/过重偏离范围）。
  static Map<int, Map<int, (double, double, double)?>> getFullTable({
    required bool isMale,
    required bool isStrengthTraining,
    required FitnessGoal goal,
  }) {
    final rawTable = _selectTable(isMale, isStrengthTraining, goal);
    final result = <int, Map<int, (double, double, double)?>>{};
    for (final hEntry in rawTable.entries) {
      final weightMap = <int, (double, double, double)?>{};
      for (final wEntry in hEntry.value.entries) {
        final val = wEntry.value;
        if (val == null) {
          weightMap[wEntry.key] = null;
        } else {
          weightMap[wEntry.key] = _normalizeFactor(val);
        }
      }
      result[hEntry.key] = weightMap;
    }
    return result;
  }

  /// 获取场景标题文字（如"男性 — 减脂（力量训练）"）
  static String scenarioTitle({
    required bool isMale,
    required bool isStrengthTraining,
    required FitnessGoal goal,
  }) {
    final genderLabel = isMale ? '男性' : '女性';
    return '$genderLabel — ${_scenarioLabel(isStrengthTraining, goal)}';
  }

  /// 根据条件选择对应的数据表
  static Map<int, Map<int, dynamic>> _selectTable(
    bool isMale,
    bool isStrengthTraining,
    FitnessGoal goal,
  ) {
    if (!isStrengthTraining) {
      return isMale ? nutritionMale : nutritionFemale;
    }
    if (goal == FitnessGoal.fatLoss) {
      return isMale ? maleFatLoss : femaleFatLoss;
    }
    return isMale ? maleMuscleGain : femaleMuscleGain;
  }

  /// 根据性别、身高、体重查表获取营养系数（保留向后兼容）
  /// 返回 (carbs_g_per_kg, protein_g_per_kg) 或 null
  static (double, double)? getFactor({
    required bool isMale,
    required int heightCm,
    required int weightKg,
  }) {
    final table = isMale ? nutritionMale : nutritionFemale;
    final heightData = table[heightCm];
    if (heightData != null) {
      final factor = heightData[weightKg];
      if (factor != null) return factor;
    }
    final sortedHeights = table.keys.toList()..sort((a, b) {
      final diffA = (a - heightCm).abs();
      final diffB = (b - heightCm).abs();
      return diffA.compareTo(diffB);
    });
    for (final h in sortedHeights) {
      final factor = table[h]![weightKg];
      if (factor != null) return factor;
    }
    return null;
  }

  /// 计算每日总需求量
  static ({double carbsG, double proteinG}) calculateDaily({
    required (double carbs, double protein) factor,
    required double weightKg,
  }) {
    return (
      carbsG: factor.$1 * weightKg,
      proteinG: factor.$2 * weightKg,
    );
  }
